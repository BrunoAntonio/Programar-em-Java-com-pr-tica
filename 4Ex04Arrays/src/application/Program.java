package application;

public class Program {
	public static void main(String[] args) {

		int[] numero = new int[10];

		for (int i = 0; i < numero.length; i++) {
			numero[i]= i+1;
			System.out.print(numero[i]+ " ");
		}
		
		System.out.println();
		System.out.println("--------------------------");
		
		String[] texto= {"Um","Dois","Três","Quatro","Cinco"};
		
		for(String s: texto) {
			System.out.print(s+ " ");
		}
		
		
		
	}

}
